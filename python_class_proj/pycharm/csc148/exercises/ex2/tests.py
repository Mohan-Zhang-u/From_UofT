def divided(x,y):
    '''

    @param x: int
    @param y: int
    @return: float

    >>> divided(4,4)
    1.0
    >>> divided(3,4)
    0.75
    >>> divided(2,3)
    0.6666666666666666
    >>> divided(2,0)
    Traceback (most recent call last):
        ...
    ZeroDivisionError: division by zero
    '''
    return x/y